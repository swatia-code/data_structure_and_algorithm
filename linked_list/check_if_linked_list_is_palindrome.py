def isPalindrome(head):
    #code here
    lis = list()
    temp = head
    while temp:
        lis.append(temp.data)
        temp = temp.next
    
       
    while head:
        if head.data != lis[-1]:
            return False
        lis.pop(-1)
        head = head.next
        
    return True

