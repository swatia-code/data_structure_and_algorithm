def height(root):
    if root is None:
        return 0
        
    left_height = height(root.left)
    right_height = height(root.right)
    
    return max(left_height, right_height) + 1
    

def isBalanced(root):
    
    #add code here
    if root is None:
        return True
        
    left_height = height(root.left)
    right_height = height(root.right)
    
    if (abs(left_height - right_height) <= 1) and isBalanced(root.left) and isBalanced(root.right):
        return True
    
    return False




